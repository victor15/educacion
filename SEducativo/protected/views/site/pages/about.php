<p>El 4 de  Mayo de 1973 el Dr. Rafael Caldera, presidente de la República, autoriza por Decreto 1.281 el funcionamiento del Instituto Universitario de Nuevas Profesiones con sede en Caracas.
 
Tenemos el orgullo de ser la primera entidad educativa, tanto a nivel público como privado, que introdujo en Venezuela las carreras de Turismo y Comercio Exterior. Los fundadores del Instituto, adelantándose a los tiempos modernos, se han dado cuenta de que el desarrollo y prosperidad de Venezuela está, principalmente, en la preparación de Técnicos Superiores Universitarios. El tiempo les ha dado la razón. Con más de 30 años de experiencia, el Instituto ha dado a Venezuela infinidad de jóvenes graduados en las distintas carreras técnicas.
 
Nuevas Profesiones se ha distinguido siempre por la calidad de su enseñanza y por la selección y preparación de su profesorado. La prueba de todo esto es el testimonio de miles de jóvenes que hoy ocupan puestos de vanguardia en la sociedad.
 
Puedes graduarte en tres años de Técnico Superior en cualquiera de las carreras que te ofrece el Instituto. El mercado laboral que tienes por delante es grande. Venezuela te espera para construir la nación grande y próspera que todos deseamos.
 
Queremos que seas un profesional que mejores la calidad de vida tuya y de los venezolanos. Por eso te invitamos a formar parte de esta familia. Te aseguramos que te encontrarás bien y aprenderás cuanto necesitas. El buen ambiente académico, docente y estudiantil te ayudará a realizarte como excelente profesional.</p>
